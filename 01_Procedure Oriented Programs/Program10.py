#Bitwise Operator
a = 60            # 60 = 0011 1100 
b = 13            # 13 = 0000 1101 

#Binary AND  :   a & b
print("Binary AND gives  :", a&b)
# Output -> 12 = 0000 1100

#Binary OR  :   a | b
print("Binary OR gives  :", a|b)
# Output -> 61 = 0011 1101 

#Binary XOR   :   a ^ b
print("Binary XOR gives  :", a^b)
# Output -> 49 = 0011 0001

#Binary NOT  :  ~ a, ~ b
print("Binary NOT gives  :", ~a)
# Output -> -61 = 1100 0011
print("Binary NOT gives  :", ~b)
# Output -> -14 = 1100 0011

#Binary LEFT SHIFT  :   a<<2
print("Binary Left Shift gives  :", a<<2)
# Output -> 240 = 1111 0000

#Binary RIGHT SHIFT  :   a>>2
print("Binary Right Shift gives  :", a>>2)
# Output -> 15 = 0000 1111


