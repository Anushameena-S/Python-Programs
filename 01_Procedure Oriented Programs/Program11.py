#Identity Operator
today = "yoga day"
wednesday="yoga day"
sunday="holiday"
a=10
b=10
c=20
#is
print("Is Wednesday Yoga Day  :", today is wednesday)
print("Is Sunday Yoga Day  :", today is sunday)
print("Is a value 10  :", a is b)
print("Is a value 20  :", a is c)
#is not
print("Is Wednesday Not Yoga Day  :", today is not wednesday)
print("Is Sunday Not Yoga Day  :", today is not sunday)
print("Is a value Not 10  :", a is not b)
print("Is a value Not 20  :", a is not c)

